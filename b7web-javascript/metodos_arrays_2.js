// Remove um item da lista

let lista = ['Ovo', 'Banana', 'Maça', 'Uva'];

lista.splice(0, 1);

let res = lista;

console.log(res)


//Concatenando Arrays

let lista = ['Ovo', 'Banana', 'Maça', 'Uva'];
let lista2 = ['Bola', 'Vassoura', 'Fogão']


let res = lista.concat(lista2);

console.log(res)

// Ordenar em ordem alfabetica, Inverter a ordem 

let lista = ['Ovo', 'Banana', 'Maça', 'Uva'];

lista.sort();
lista.reverse();

let res = lista;

console.log(res)



